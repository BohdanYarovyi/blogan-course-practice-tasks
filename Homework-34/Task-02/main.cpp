#include <iostream>

/*
Реалізуємо геометричну прогресію. Ваша задача вивести на екран числа:
	2 4 8 16 32 64 128
І таких чисел має бути 20. Тобто перше число = 2. Кожне наступне число - це попереднє * 2.
*/

int main() {
	int number = 2;

	for (int i = 0; i < 20; i++) {
		std::cout << number << std::endl;
		number *= 2;
	}

	return 0;
}