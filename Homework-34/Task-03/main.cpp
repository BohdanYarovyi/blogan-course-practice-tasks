#include <iostream>

/*
Перепишіть попередню задачу, щоб кожне наступне число - це попереднє помножене саме на себе. 
Тобто ось так:
	2 4 16 256 65536 і так далі
Таких чисел має бути 10.

А що, щось не так з виводом? :)
*/

int main() {
	long long number = 2;

	for (int i = 0; i < 20; i++) {
		std::cout << number << std::endl;
		number *= number;
	}

	return 0;
}